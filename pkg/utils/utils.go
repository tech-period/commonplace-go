package utils

// ユーティリティ関数を定義します。

// ExampleFunction はサンプルのユーティリティ関数です。
func ExampleFunction(param string) string {
    return "Hello, " + param
}

// Add は2つの整数を加算するユーティリティ関数です。
func Add(a int, b int) int {
    return a + b
}

// Subtract は2つの整数を減算するユーティリティ関数です。
func Subtract(a int, b int) int {
    return a - b
}